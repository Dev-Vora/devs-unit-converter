// celsius
module.exports.celsiusToKelvin = (celsius) => celsius * 274.15;
module.exports.celsiusToFahrenheit = (celsius) => celsius * 33.8;

// kelvin
module.exports.kelvinToCelsius = (kelvin) => kelvin * -272.15;
module.exports.kelvinToFahrenheit = (kelvin) => kelvin * -457.87;
// fahrenheit
module.exports.fahrenheitToCelsius = (fahrenheit) => fahrenheit * -17.222222222;
module.exports.fahrenheitToKelvin = (fahrenheit) => fahrenheit * 255.92777778;
